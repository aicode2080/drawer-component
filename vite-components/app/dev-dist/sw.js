// If the loader is already loaded, just stop.
if (!self.define) {
  let registry = {};

  // Used for `eval` and `importScripts` where we can't get script URL by other means.
  // In both cases, it's safe to use a global var because those functions are synchronous.
  let nextDefineUri;

  const singleRequire = (uri, parentUri) => {
    uri = new URL(uri + '.js', parentUri).href;
    return (
      registry[uri] ||
      new Promise((resolve) => {
        if ('document' in self) {
          const script = document.createElement('script');
          script.src = uri;
          script.onload = resolve;
          document.head.appendChild(script);
        } else {
          nextDefineUri = uri;
          importScripts(uri);
          resolve();
        }
      }).then(() => {
        let promise = registry[uri];
        if (!promise) {
          throw new Error(`Module ${uri} didn’t register its module`);
        }
        return promise;
      })
    );
  };

  self.define = (depsNames, factory) => {
    const uri = nextDefineUri || ('document' in self ? document.currentScript.src : '') || location.href;
    if (registry[uri]) {
      // Module is already loading or loaded.
      return;
    }
    let exports = {};
    const require = (depUri) => singleRequire(depUri, uri);
    const specialDeps = {
      module: { uri },
      exports,
      require,
    };
    registry[uri] = Promise.all(depsNames.map((depName) => specialDeps[depName] || require(depName))).then((deps) => {
      factory(...deps);
      return exports;
    });
  };
}
define(['./workbox-a186dfd7'], function (workbox) {
  'use strict';

  workbox.setCacheNameDetails({
    prefix: 'pwa-vite-component',
  });
  self.skipWaiting();
  workbox.clientsClaim();

  /**
   * The precacheAndRoute() method efficiently caches and responds to
   * requests for URLs in the manifest.
   * See https://goo.gl/S9QRab
   */
  workbox.precacheAndRoute(
    [
      {
        url: 'index.html',
        revision: '0.okav974dfh',
      },
    ],
    {},
  );
  workbox.cleanupOutdatedCaches();
  workbox.registerRoute(
    new workbox.NavigationRoute(workbox.createHandlerBoundToURL('index.html'), {
      allowlist: [/^\/$/],
    }),
  );

  self.addEventListener("activate", (event) => {
    console.log('事件：', event);
    event.waitUntil(
      caches.keys().then((cacheGroup) => {
        console.log(cacheGroup,'=====缓存====');
        return Promise.all(
          cacheGroup
            .filter((cacheName) => {
              return !Object.values(currentCacheNames).includes(`${cacheName}`)
            })
            .map((cacheName) => {
              return caches.delete(cacheName)
            }),
        )
      }),
    )
  })


  // 首页banner
registerRoute(
  /https?:\/\/127\.0\.0\.0\/api\/banner\?type=1/,
  new CacheFirst({
    cacheName: currentCacheNames["api-banner"],
    plugins: [
      new BroadcastUpdatePlugin(),
      new ExpirationPlugin({
        maxAgeSeconds: 60 * 60 * 0.5,
        purgeOnQuotaError: true,
      }),
    ],
  }),
)

  workbox.registerRoute(
    ({ url }) => url.origin === 'https://app-api-0.com',
    new workbox.NetworkFirst({
      cacheName: cacheNames['drawer-api'],
      plugins: [
        new workbox.CacheableResponsePlugin({
          statuses: [200],
        }),
      ],
    }),
    'GET',
  );
  workbox.registerRoute(
    /\.(?:png|jpg|jpeg|svg)$/,
    new workbox.CacheFirst({
      cacheName: cacheNames['drawer-images'],
      plugins: [
        new workbox.ExpirationPlugin({
          maxEntries: 30,
        }),
      ],
    }),
    'GET',
  );
  workbox.registerRoute(
    /.*\.js.*/,
    new workbox.StaleWhileRevalidate({
      cacheName: cacheNames['drawer-js'],
      plugins: [
        new workbox.ExpirationPlugin({
          maxEntries: 30,
          maxAgeSeconds: 2592000,
        }),
        new workbox.CacheableResponsePlugin({
          statuses: [200],
        }),
      ],
    }),
    'GET',
  );
  workbox.registerRoute(
    /.*\.css.*/,
    new workbox.StaleWhileRevalidate({
      cacheName: cacheNames['drawer-css'],
      plugins: [
        new workbox.ExpirationPlugin({
          maxEntries: 20,
          maxAgeSeconds: 2592000,
        }),
        new workbox.CacheableResponsePlugin({
          statuses: [200],
        }),
      ],
    }),
    'GET',
  );
  workbox.registerRoute(
    /.*\.html.*/,
    new workbox.StaleWhileRevalidate({
      cacheName: cacheNames['drawer-html'],
      plugins: [
        new workbox.ExpirationPlugin({
          maxEntries: 20,
          maxAgeSeconds: 2592000,
        }),
        new workbox.CacheableResponsePlugin({
          statuses: [200],
        }),
      ],
    }),
    'GET',
  );
});
